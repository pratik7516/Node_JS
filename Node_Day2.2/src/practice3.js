const add = (n1, n2) => {

    return (n1 + n2);
}

const sub = (n1, n2) => {

    return (n1 - n2);
}

const multiply = (n1, n2) => {

    return (n1 * n2);
}

const division = (n1, n2) => {

    return (n1 / n2);
}

module.exports = {

    add,
    sub,
    multiply,
    division
}